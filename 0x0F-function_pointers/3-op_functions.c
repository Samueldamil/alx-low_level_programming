#include "3-calc.h"
/**
 * op_add - add two integers
 * @a : integer
 * @b : integer
 * Return: sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract b from a
 * @a : integer
 * @b : integer
 * Return: differences between two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: integer
 * @b: integer
 * Return: answer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a by b
 * @a: integer
 * @b: integer
 * Return: answer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - find the remainder
 * @a : integer
 * @b : integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
